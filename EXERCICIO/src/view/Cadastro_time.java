    private void limparCampos() {
        txCodigo.setText("");
        txNome.setText("");
        txMascote.setText("");
        txFundacao.setText("");
        txEstadio.setText("");
        txLocalizacao.setText("");
        txTreinador.setText("");
    }

    public Time_futebol criarTime() {
        int codigo = Integer.parseInt(txCodigo.getText());
        String nome = txNome.getText();
        String mascote = txMascote.getText();
        String fundacao = txFundacao.getText();
        String estadio = txEstadio.getText();
        String localizacao = txLocalizacao.getText();
        String treinador = txTreinador.getText();

        return new Time_futebol(codigo, nome, mascote, fundacao, estadio, localizacao, treinador);
    }

