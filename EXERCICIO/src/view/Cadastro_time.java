    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        if (txCodigo.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Você não pode gravar sem um codigo");
        } else if (txNome.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Você não pode gravar sem um nome");
        } else if (txMascote.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Você não pode gravar sem um mascote");
        } else if (txFundacao.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Você não pode gravar sem uma data de fundação");
        } else if (txEstadio.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Você não pode gravar sem um estádio");
        } else if (txLocalizacao.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Você não pode gravar sem uma localização");
        } else if (txTreinador.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Você não pode gravar sem um treinador");
        } else {
            time_futebol = criarTime();
            JOptionPane.showMessageDialog(this, time_futebol);
            resgatarTime(time_futebol);
            desabilitarComponentes();
            limparCampos();
        }
    }//GEN-LAST:event_btSalvarActionPerformed

