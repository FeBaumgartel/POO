    public void resgatarTime(Time_futebol a) {
        txCodigo.setText(String.valueOf(a.getId()));
        txNome.setText(a.getNome());
        txMascote.setText(a.getMascote());
        txFundacao.setText(a.getFundacao());
        txEstadio.setText(a.getEstadio());
        txLocalizacao.setText(a.getLocalizacao());
        txTreinador.setText(a.getTreinador());
        }

