    private void desabilitarComponente() {
        txCodigo.setEnabled(false);
        txNome.setEnabled(false);

        btCancelar.setEnabled(false);
        btSalvar.setEnabled(false);
    }

    private void habilitarComponente() {
        txCodigo.setEnabled(true);
        txNome.setEnabled(true);

        btCancelar.setEnabled(true);
        btSalvar.setEnabled(true);
    }

    private void limpaCampos() {
        txCodigo.setText("");
        txNome.setText("");
    }

    private Aluno criarAluno() {
        int codigo = 0;
        String nome = null;
        try {
            codigo = Integer.parseInt(txCodigo.getText());
            nome = txNome.getText();
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, ex, ex.getMessage(),
                    JOptionPane.ERROR_MESSAGE, null);
        }
        return new Aluno(codigo, nome);
    }

